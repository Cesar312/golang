package main

import "fmt"

func main() {

	x, y, z := 747, 911, 90210

	fmt.Printf("Value: %v\t Decimal: %d\t Binary: %b\t\t Hexadecimal: %#X\n", x, x, x, x)
	fmt.Printf("Value: %v\t Decimal: %d\t Binary: %b\t\t Hexadecimal: %#X\n", y, y, y, y)
	fmt.Printf("Value: %v\t Decimal: %d\t Binary: %b\t Hexadecimal: %#X\n", z, z, z, z)
}
